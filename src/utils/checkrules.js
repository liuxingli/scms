export const convertBoolean = (valuestr) => {
  const lowcase = valuestr.toLowerCase()
  if (lowcase === 'true' || lowcase === '1') {
    return '1'
  } else {
    return '0'
  }
}

export const isvalidateBoolean = (rule, value, callback) => {
  if (value != null && value !== '') {
    const lowcase = value.toLowerCase()
    if (lowcase !== 'false' && lowcase !== 'true' && lowcase !== '0' && lowcase !== '1') {
      callback(new Error('invalid boolean'))
    } else {
      callback()
    }
  }
}

function IsURL(str_url) {
  var strRegex = '^((https|http|ftp|rtsp|mms)?://)' +
  '?(([0-9a-z_!~*\'().&=+$%-]+: )?[0-9a-z_!~*\'().&=+$%-]+@)?' +
  '(([0-9]{1,3}.){3}[0-9]{1,3}' + // IP URL- 199.194.52.184
  '|' + // IP and DOMAIN
  '([0-9a-z_!~*\'()-]+.)*' + // www.
  '([0-9a-z][0-9a-z-]{0,61})?[0-9a-z].' +
  '[a-z]{2,6})' + // first level domain- .com or .museum
  '(:[0-9]{1,4})?' + // port- :80
  '((/?)|' + // a slash isn't required if there is no file name
  '(/[0-9a-z_!~*\'().;?:@&=+$,%#-]+)+/?)$'
  var re = new RegExp(strRegex)
  if (re.test(str_url)) {
    return (true)
  } else {
    return (false)
  }
}

export const isvalidateURL = (rule, value, callback) => {
  if (value != null && value !== '') {
    if (!IsURL(value)) {
      callback(new Error('invalid url'))
    } else {
      callback()
    }
  } else {
    callback()
  }
}

